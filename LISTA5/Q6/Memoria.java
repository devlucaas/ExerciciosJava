package LISTA5.Q6;

public class Memoria {
    int memoria;
    String marca;

    public Memoria(){
    }

    public Memoria(int memoria, String marca) {
        this.memoria = memoria;
        this.marca = marca;
    }

    public int getMemoria() {
        return memoria;
    }

    public void setMemoria(int memoria) {
        this.memoria = memoria;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    
}
