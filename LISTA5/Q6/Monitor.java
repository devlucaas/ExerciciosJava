package LISTA5.Q6;

public class Monitor {
    int polegadas;
    int hertz;

    public Monitor(){
    }

    public Monitor(int polegadas, int hertz) {
        this.polegadas = polegadas;
        this.hertz = hertz;
    }

    public int getPolegadas() {
        return polegadas;
    }

    public void setPolegadas(int polegadas) {
        this.polegadas = polegadas;
    }

    public int getHertz() {
        return hertz;
    }

    public void setHertz(int hertz) {
        this.hertz = hertz;
    }

    
}
